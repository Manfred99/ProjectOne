    public void autoComplete() throws IOException{
        TextAutoCompleter complete = new TextAutoCompleter(tfd_search);
        File file = new File("./AudioVisual.dat");
        AudioVisualFile audioFile = new AudioVisualFile(file);
        ArrayList<AudioVisual> myArrayList = audioFile.getAudioList();
        AudioVisual audioTemp = new AudioVisual();
        for(int i= 0;i<myArrayList.size();i++){
            audioTemp = myArrayList.get(i);
            complete.addItem(audioTemp.getName());
            complete.addItem(audioTemp.getCode());
        }
    }//end method
