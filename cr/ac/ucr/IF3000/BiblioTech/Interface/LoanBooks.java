    public void insertLoan(){
        try {
            File file = new File("./Books.dat");
            BooksFile bookFile = new BooksFile(file);
            ArrayList<Books> myArrayList = bookFile.getBooksList();
            Books bookTemp = new Books();
            for(int i= 0;i<myArrayList.size();i++){
                if(myArrayList.get(i).getTitle().equals(tfd_book.getText()))
                    bookTemp = myArrayList.get(i);
            }
            if(bookTemp == null){
                System.err.println("The book couldn't be found in the file");
            }else{
                RequestBookFile loanBook = new RequestBookFile();
                String formato = "dd/MM/yyyy";
                String card_student = tfd_idStudent.getText();
                Date date1 = jd_removing.getDate();
                Date date2 = jd_delivery.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat(formato);
                String date_removing = sdf.format(date1);
                String date_delivery = sdf.format(date2);
                String titleBook = tfd_book.getText();
                String authors= bookTemp.getAuthors();
                int amount = bookTemp.getAmount();
                ArrayList<RequestBook> listLoan = loanBook.getLoanList();
                int available = 0;
                if(listLoan.size()>0){
                    for(int c=0;c<listLoan.size();c++){
                        if(listLoan.get(c).getTitle().equals(titleBook)){
                            available = listLoan.get(c).getAvailable()-1;
                        }
                    }
                }else{
                    available = bookTemp.getAmount()-1;
                }
                RequestBook rBook = new RequestBook(card_student, available, date_removing, date_delivery, titleBook, authors, bookTemp.getType(), bookTemp.getCode(), amount, bookTemp.getAge());
                if(available>=0){
                    loanBook.insertLoanBook(rBook);
                }
                else{
                    System.err.println("DonÂ´t found book available");
                }
                tfd_search.setText("");
                tfd_book.setText("");
                jd_removing.setDateFormatString("");
                jd_delivery.setDateFormatString("");
                tfd_search.requestFocus();
            }
        } catch (Exception e) {
        }
    }
    
    private void tfd_searchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfd_searchKeyTyped
        String titleBook = tfd_search.getText();
        String book = "";
        try {
            File file = new File("./Books.dat");
            BooksFile bookFile = new BooksFile(file);
            ArrayList<Books> listOfBook = bookFile.getBooksList();
            for(int i=0;i<listOfBook.size();i++){
                if(listOfBook.get(i).getCode().equals(titleBook)){
                    book=listOfBook.get(i).getTitle();
                }else if(listOfBook.get(i).getTitle().equals(titleBook)){
                    book=listOfBook.get(i).getTitle();
                }
            }
        } catch (IOException e) {
        }
        
        tfd_book.setText(book);
          
    }//GEN-LAST:event_tfd_searchKeyTyped

