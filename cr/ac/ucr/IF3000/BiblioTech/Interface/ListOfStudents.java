    private void cb_choiseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_choiseActionPerformed
        // TODO add your handling code here:
        String item=cb_choise.getSelectedItem().toString();
        try {
             File file = new File("./student.dat");
             StudentFile st = new StudentFile(file);

            
        
        if(item.equals("Agronomy")){
            limpiarTabla();
            showTable(lvl_StudentList,st.ordenaAlfabeticamente(st.getMayors("A")));
        }else if (item.equals("Computing")){
            limpiarTabla();
            showTable(lvl_StudentList,st.ordenaAlfabeticamente(st.getMayors("I")));
        }else if (item.equals("Education")){
            limpiarTabla();
            showTable(lvl_StudentList,st.ordenaAlfabeticamente(st.getMayors("E")));
        }
        } catch (IOException e) {
            System.err.println("No match found vehicles");
        }

    }//GEN-LAST:event_cb_choiseActionPerformed

