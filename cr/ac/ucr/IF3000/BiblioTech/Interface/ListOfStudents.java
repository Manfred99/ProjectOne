    public void showTable(JTable table, ArrayList<Student> a) throws IOException {
        File file = new File("./student.dat");
        StudentFile studentFile = new StudentFile(file);
        Student student = new Student();
        ArrayList<Student> myArrayList = a;
        
        DefaultTableModel modelo = (DefaultTableModel) table.getModel();
        table = new JTable(modelo);
        for(int i=0;i<myArrayList.size();i++){
            student = myArrayList.get(i);
            Object obj[] = new Object[]{
                student.getName(),student.getMayor(),student.getYearIncome(),student.getId()};
            modelo.addRow(obj);
        }
        
        lvl_StudentList.setModel(modelo);
    }
